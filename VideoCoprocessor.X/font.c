void drawCharacter(uint8_t posX, uint8_t posY, uint8_t character) {
    if (character < '!' || character > '~') {
        return;
    }
    character -= '!';
    uint16_t tempFontIndex = character * 7;
    uint16_t tempVideoIndex = (posX >> 3) + posY * VIDEO_BUFFER_WIDTH;
    uint8_t tempShiftAmount = 11 - (posX & 0x07);
    uint8_t tempCount = 0;
    while (tempCount < 7) {
        int16_t tempData = fontData[tempFontIndex];
        tempData <<= tempShiftAmount;
        videoData[tempVideoIndex] |= (tempData & 0xFF00) >> 8;
        videoData[tempVideoIndex + 1] |= tempData & 0x00FF;
        tempFontIndex += 1;
        tempVideoIndex += VIDEO_BUFFER_WIDTH;
        tempCount += 1;
    }
}


